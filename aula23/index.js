//Operadores de comparação
// > maior que
console.log(1 > 2); // false

// < menor que
console.log(1 < 2); // true

// >= maior ou igual
console.log(1 >= 2); // false

// <= menor ou igual
console.log(1 <= 2); // true

// == igual
const v1 = 1;
const v2 = '1';
// Não é uma boa prática usar o ==
console.log(v1 == v2); // true

// === igual e do mesmo tipo
console.log(v1 === v2); // false

// != diferente
// Não é uma boa prática usar o !=
console.log(v1 != v2); // false

// !== diferente e do mesmo tipo
console.log(v1 !== v2); // true
